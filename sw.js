// Push notifications (for wallpaper updates)
self.addEventListener('push', event => {
  const options = {
    body: event.data ? event.data.text() : 'Wallpaper nou disponibil!',
    icon: '/icons/icon-192.png',
    badge: '/icons/icon-72.png',
    vibrate: [100, 50, 100],
    data: {
      dateOfArrival: Date.now(),
      primaryKey: 1
    }
  };

  event.waitUntil(
    self.registration.showNotification('Smart Wallpaper', options)
  );
});